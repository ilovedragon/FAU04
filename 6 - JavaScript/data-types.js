// Data Types - Primitive

// Number: Represents numeric value (e.g. 12, 100000, -24, 1.55)
let age = 25;
console.log(age);

age = 5.5;
console.log(age);
console.log("The type of age is:", typeof(age));

// Strings: Represent text/textual values enclosed in '' or "" (e.g. "abc123!@#")
let name = "Rick";
console.log("The value of name is:",name);
console.log("The type of age is:", typeof(name));

// Boolean: Either true or false
let isAdmin = true;
console.log("The value of isAdmin is:", isAdmin);

isAdmin = false;
console.log("The value of isAdmin is:", isAdmin);
console.log("The type of isAdmin is:", typeof(isAdmin));

// Null: Represents absence of value (intentional)
let car = null;
console.log("The value of car is:", car);
console.log("The type of car is:", typeof(car));

// Undefined: Represents absence of value (unintentional)
let city;
console.log("The value of city is:", city);
console.log("The type of city is:", city);
